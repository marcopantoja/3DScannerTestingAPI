class Units():
    """
    For representing various units of measurement and conversions 
    from listed unit to device units.
    """
    
    NATIVE_UNITS = 1.0
    
    # Length Conversions:
    LENGTH_MILLIMETRES = 100.0              # to go from mm to device units multiply by 10.


    # Angle Conversions:
    ANGLE_RADIANS = 5729.5779513082325   # 180 / pi * 100 to go from radians to device units
    ANGLE_DEGREES = 10                  # to go from degrees to device units multiiply degrees by 100

